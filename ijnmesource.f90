       &)*Q(2,3)+C(6)*Q(3,3))
  CE(4)=Q(3,2)*V(108)+Q(2,2)*V(109)+Q(1,2)*V(110)
  CE(5)=Q(3,3)*V(108)+Q(2,3)*V(109)+Q(1,3)*V(110)
  CE(6)=Q(3,3)*V(111)+Q(2,3)*V(112)+Q(1,3)*V(113)
  DCEDC(1,1)=Q(1,1)**2
  DCEDC(1,2)=Q(2,1)**2
  DCEDC(1,3)=Q(3,1)**2
  DCEDC(1,4)=Q(1,1)*V(114)
  DCEDC(1,5)=2D0*Q(1,1)*Q(3,1)
  DCEDC(1,6)=Q(3,1)*V(114)
  DCEDC(2,1)=Q(1,2)**2
  DCEDC(2,2)=Q(2,2)**2
  DCEDC(2,3)=Q(3,2)**2
  DCEDC(2,4)=Q(1,2)*V(115)
  DCEDC(2,5)=2D0*Q(1,2)*Q(3,2)
  DCEDC(2,6)=Q(3,2)*V(115)
  DCEDC(3,1)=Q(1,3)**2
  DCEDC(3,2)=Q(2,3)**2
  DCEDC(3,3)=Q(3,3)**2
  DCEDC(3,4)=Q(1,3)*V(116)
  DCEDC(3,5)=2D0*Q(1,3)*Q(3,3)
  DCEDC(3,6)=Q(3,3)*V(116)
  DCEDC(4,1)=Q(1,1)*Q(1,2)
  DCEDC(4,2)=Q(2,1)*Q(2,2)
  DCEDC(4,3)=Q(3,1)*Q(3,2)
  DCEDC(4,4)=Q(1,2)*Q(2,1)+Q(1,1)*Q(2,2)
  DCEDC(4,5)=Q(1,2)*Q(3,1)+Q(1,1)*Q(3,2)
  DCEDC(4,6)=Q(2,2)*Q(3,1)+Q(2,1)*Q(3,2)
  DCEDC(5,1)=Q(1,1)*Q(1,3)
  DCEDC(5,2)=Q(2,1)*Q(2,3)
  DCEDC(5,3)=Q(3,1)*Q(3,3)
  DCEDC(5,4)=Q(1,3)*Q(2,1)+Q(1,1)*Q(2,3)
  DCEDC(5,5)=Q(1,3)*Q(3,1)+Q(1,1)*Q(3,3)
  DCEDC(5,6)=Q(2,3)*Q(3,1)+Q(2,1)*Q(3,3)
  DCEDC(6,1)=Q(1,2)*Q(1,3)
  DCEDC(6,2)=Q(2,2)*Q(2,3)
  DCEDC(6,3)=Q(3,2)*Q(3,3)
  DCEDC(6,4)=Q(1,3)*Q(2,2)+Q(1,2)*Q(2,3)
  DCEDC(6,5)=Q(1,3)*Q(3,2)+Q(1,2)*Q(3,3)
  DCEDC(6,6)=Q(2,3)*Q(3,2)+Q(2,2)*Q(3,3)
END SUBROUTINE CEFROMC

