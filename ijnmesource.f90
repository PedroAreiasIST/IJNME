
!---------------------------------------------
!*** DERIVATIVES OF PHI WITH RESPECT TO CE
!*** AND NORM (flow vector) WITH RESPECT TO CE
!---------------------------------------------
SUBROUTINE WRTOCANDCE(DPHIDCE,DNORMDCE,DMANDELDCE,DPHIDMANDEL,DNORMDMANDEL)  
  IMPLICIT REAL(8)(A-H,O-Z)
  REAL(8),DIMENSION(6)::DPHIDCE
  REAL(8),DIMENSION(3,3,6)::DNORMDCE
  REAL(8),DIMENSION(3,3,6)::DMANDELDCE
  REAL(8),DIMENSION(3,3)::DPHIDMANDEL
  REAL(8),DIMENSION(3,3,3,3)::DNORMDMANDEL
  DPHIDCE=0.0D00
  DO I=1,6
     DO K=1,3
        DO J=1,3
           DPHIDCE(I)=DPHIDCE(I)+DPHIDMANDEL(J,K)*DMANDELDCE(J,K,I)
        END DO
     END DO
  END DO
  DNORMDCE=0.0D00
  DO M=1,6
     DO L=1,3
        DO K=1,3
           DO J=1,3
              DO I=1,3
                 DNORMDCE(I,J,M)=DNORMDCE(I,J,M)+DNORMDMANDEL(I,J,K,L)*DMANDELDCE(K,L,M)
              END DO
           END DO
        END DO
     END DO
  END DO
END SUBROUTINE WRTOCANDCE

!------------------------------
!*** CE AND DCEDC FROM C AND Q
!------------------------------
SUBROUTINE CEFROMC(C,Q,CE,DCEDC)
  IMPLICIT NONE
  DOUBLE PRECISION V(121),C(6),Q(3,3),CE(6),DCEDC(6,6)
  V(116)=2D0*Q(2,3)
  V(115)=2D0*Q(2,2)
  V(114)=2D0*Q(2,1)
  V(113)=C(1)*Q(1,2)+C(4)*Q(2,2)+C(5)*Q(3,2)
  V(112)=C(4)*Q(1,2)+C(2)*Q(2,2)+C(6)*Q(3,2)
  V(111)=C(5)*Q(1,2)+C(6)*Q(2,2)+C(3)*Q(3,2)
  V(110)=C(1)*Q(1,1)+C(4)*Q(2,1)+C(5)*Q(3,1)
  V(109)=C(4)*Q(1,1)+C(2)*Q(2,1)+C(6)*Q(3,1)
  V(108)=C(5)*Q(1,1)+C(6)*Q(2,1)+C(3)*Q(3,1)
  CE(1)=Q(3,1)*V(108)+Q(2,1)*V(109)+Q(1,1)*V(110)
